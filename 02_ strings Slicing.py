name="HarryIsGood"
# Performing Slicing
print(name[0:3])
print(name [:5])  #is Same as [0:4]
print(name [:4])  #is Same as [0:4]
print(name [0:]) #is Same as [0:4]
print(name [-5:-1]) # is same as name [0:4]
print(name[-4:-2])  # is same as name [1:3]
# Slicing with skip value
d=name[1::2]
print(d)

